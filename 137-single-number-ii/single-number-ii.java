class Solution {
    public int singleNumber(int[] nums) {
        HashMap<Integer,Integer>map=new HashMap<>();
        for(int x: nums){
            map.put(x,map.getOrDefault(x,0)+1);
        }
        for(int y: map.keySet()){
            if(map.get(y)==1){
                return y;
            }
        }
        return -1;
    }
}